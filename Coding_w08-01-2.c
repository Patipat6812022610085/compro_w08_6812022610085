#include <stdio.h>

int main() {
for (int i = 2; i <= 10; i += 2) {
printf("%d ", i); 
}

printf("\nEnd of loop\n"); 

return 0; 
}

/*
คำอธิบายโปรแกรม:
1. ใช้ for loop โดย:
- กำหนดค่าเริ่มต้น i = 2
- กำหนดเงื่อนไข i <= 10
- เพิ่มค่า i ขึ้นทีละ 2 ในแต่ละรอบ (i += 2)
2. ในแต่ละรอบของลูป แสดงค่าปัจจุบันของ i
3. หลังจากออกจากลูป แสดงข้อความ "End of loop"
4. ส่งค่า 0 กลับเพื่อระบุว่าโปรแกรมทำงานสำเร็จ

เปรียบเทียบกับ while loop:
- for loop รวบรวมการกำหนดค่าเริ่มต้น เงื่อนไข และการเพิ่มค่าไว้ในบรรทัดเดียวกัน
- while loop แยกส่วนต่างๆ ออกมาชัดเจนกว่า
- ทั้งสองวิธีให้ผลลัพธ์เดียวกัน
*/