#include <stdio.h>

int main() {
int i = 2;

while (i <= 10) {
printf("%d ", i); 
i += 2; 
}

printf("\nEnd of loop\n"); 

return 0; 
}

/*
คำอธิบายโปรแกรม:
1. ประกาศตัวแปร i และกำหนดค่าเริ่มต้นเป็น 2
2. ใช้ while loop โดยให้ทำงานเมื่อ i น้อยกว่าหรือเท่ากับ 10
3. ในแต่ละรอบของลูป:
- แสดงค่าปัจจุบันของ i
- เพิ่มค่า i ขึ้นทีละ 2 เพื่อให้ได้เลขคู่ลำดับถัดไป
4. หลังจากออกจากลูป แสดงข้อความ "End of loop"
5. ส่งค่า 0 กลับเพื่อระบุว่าโปรแกรมทำงานสำเร็จ
*/